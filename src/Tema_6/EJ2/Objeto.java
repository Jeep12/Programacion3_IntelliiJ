package Tema_6.EJ2;

public class Objeto {

    Double valor;
    Double peso;


    public Objeto(Double valor, Double peso) {
        this.valor = valor;
        this.peso = peso;
    }

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }

    public Double getPeso() {
        return peso;
    }

    public void setPeso(Double peso) {
        this.peso = peso;
    }
}
